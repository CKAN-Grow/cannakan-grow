import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const PROFILE_COLUMN_DEFINITIONS = [
  { name: "username", definition: "text not null default ''" },
  { name: "email", definition: "text default ''" },
  { name: "avatar_url", definition: "text default ''" },
  { name: "avatar_path", definition: "text default ''" },
  { name: "account_status", definition: "text not null default 'active'" },
  { name: "last_active_at", definition: "timestamptz" },
  { name: "deletion_requested_at", definition: "timestamptz" },
  { name: "deletion_scheduled_for", definition: "timestamptz" },
  { name: "deletion_status", definition: "text default ''" },
  { name: "created_at", definition: "timestamptz not null default timezone('utc', now())" },
  { name: "updated_at", definition: "timestamptz not null default timezone('utc', now())" },
];

const PROFILE_INDEX_DEFINITIONS = [
  "create index if not exists profiles_account_status_idx\n  on public.profiles (account_status, created_at desc);",
  "create index if not exists profiles_last_active_idx\n  on public.profiles (last_active_at desc);",
];

const workspaceRoot = process.cwd();
const schemaPath = resolve(workspaceRoot, "supabase-schema.sql");
const outputPath = resolve(workspaceRoot, "supabase-profile-columns-migration.sql");

const schemaContents = readFileSync(schemaPath, "utf8");
const createTableMatch = schemaContents.match(/create table if not exists public\.profiles \(([\s\S]*?)\n\);/i);
const profilesCreateTableBody = createTableMatch?.[1] || "";

const locallyMissingColumns = PROFILE_COLUMN_DEFINITIONS.filter(({ name }) => {
  const createTableRegex = new RegExp(`^\\s*${name}\\b`, "im");
  const alterTableRegex = new RegExp(`alter table public\\.profiles\\s+add column if not exists ${name}\\b`, "i");
  return !createTableRegex.test(profilesCreateTableBody) && !alterTableRegex.test(schemaContents);
});

const generatedSections = [
  "-- Auto-generated by scripts/generate-profile-columns-migration.mjs",
  `-- Generated at ${new Date().toISOString()}`,
  "-- Safe to run repeatedly because every statement is idempotent.",
  "",
  ...PROFILE_COLUMN_DEFINITIONS.flatMap(({ name, definition }) => ([
    "alter table public.profiles",
    `  add column if not exists ${name} ${definition};`,
    "",
  ])),
  ...PROFILE_INDEX_DEFINITIONS.flatMap((statement) => ([statement, ""])),
];

if (locallyMissingColumns.length) {
  generatedSections.push(
    "-- The following app-used profile columns were not found in supabase-schema.sql when this migration was generated:",
    ...locallyMissingColumns.map(({ name }) => `-- - ${name}`),
    "",
  );
  console.warn(
    `[profile-migration] supabase-schema.sql is missing local definitions for: ${locallyMissingColumns.map(({ name }) => name).join(", ")}`,
  );
}

writeFileSync(outputPath, `${generatedSections.join("\n").trim()}\n`, "utf8");
console.log(`[profile-migration] Wrote ${outputPath}`);
